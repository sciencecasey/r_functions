prin_corr <- function(data){
  #'@param data an uncentered/unscaled matrix (prcomp automatically centers & scales it)
  #'@return a list of eigen vectors, values, and cumulative explained proportion
  R = cor(data)
  prinObj = prcomp(data, scaled = TRUE)
  vec = eigen(R)$vectors
  vals = eigen(R)$values
  explained = summary(prinObj)
  vec = prinObj$val
  explained = explained$importance["Cumulative Proportion", ] # only save proportion
  return(list(correlation = R, vec = vec, vals = vals, explained = explained))
}

prin_cov <- function(data){
  #'@param data an uncentered/unscaled matrix (prcomp automatically centers & scales it)
  #'@return a list of eigen vectors, values, and cumulative explained proportion
  C = cov(data)
  prinObj = prcomp(data)
  vec = eigen(C)$vectors
  vals = eigen(C)$values
  explained = summary(prinObj)
  explained = explained$importance["Cumulative Proportion", ] # only save proportion
  return(list(covariance = C, vec = vec, vals = vals, explained = explained))
}

extract_top <- function(prinObj, n = NULL, percent= NULL){
  #' @param principle comopnent object made with covariance prin_cov above (not correlation!)
  #' @param n the number of components to keep (note if both this and percent are filled only n is used)
  #' @param percent of variance to keep for components (only used if filled and n is null)
  #' @return the eigenvalues only of the principle component object 
    if(!is.null(n)){
      items = n
      return(prinObj$rotation[,1:items])
    }else{
      if(is.null(percent) && is.null(n)){
      errorCondition("n or percent (as decimal) needed")
      break
    } else if(percent>1){
      percent = percent/100 # convert to decimal
    }
    found = prinObj$explained<percent # all except one
    found = prinObj$explained[found]
    items = length(found)+1
    found = append(found, prinObj$explained[items])
    return(prinObj$vec[,1:items])
    }
}



